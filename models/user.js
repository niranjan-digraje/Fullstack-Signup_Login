// **********User Schema**********

//---------------------------------------------------------------------------------------------------------------------

// **********requirements**********

const mongoose = require("mongoose");

//---------------------------------------------------------------------------------------------------------------------

// **********MongoDB Connection**********

mongoose.connect("mongodb://127.0.0.1:27017/vkit");

//---------------------------------------------------------------------------------------------------------------------

// **********Schema creation**********

const userSchema = mongoose.Schema({
    username : String,
    name : String,
    email : String,
    password : String
});

//---------------------------------------------------------------------------------------------------------------------

// **********Exports to database**********

module.exports = mongoose.model('user',userSchema);

//---------------------------------------------------------------------------------------------------------------------